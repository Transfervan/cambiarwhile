package cambiarwhile;

public class cambiarwhile {

		public static void main(String[] args) {
			int i = 0;
			// miestras i sea menor que 50 hara ciclo, llega hasta 50 y se sale
			while (i<50) {
			i+=1;
			System.out.printf("Iteración %d\n", i);
	}
		
		i = 1;
		// muestra mientras i sea menor o igual a 50
		do {
			System.out.printf("Iteración %d\n", i);
			i+=1;
		 }while  (i<=50);
		 }


}




